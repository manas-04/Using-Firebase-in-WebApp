class Student {
  constructor(
    id,
    firstName,
    lastName,
    fatherName,
    enrollmentNo,
    age,
    phone,
    semester,
    status
  ) {
    (this.id = id),
      (this.firstName = firstName),
      (this.lastName = lastName),
      (this.fatherName = fatherName),
      (this.enrollmentNo = enrollmentNo),
      (this.age = age),
      (this.phone = phone),
      (this.semester = semester),
      (this.status = status);
  }
}
